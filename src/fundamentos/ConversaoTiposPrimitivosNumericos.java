package fundamentos;

public class ConversaoTiposPrimitivosNumericos {
    public static void main(String[] args) {
        double a = 1;
        System.out.println(a);

        float b = (float) 1.2345;
        System.out.println(b);

        int c = 129;
        byte d = (byte) c;
        System.out.println(d);
    }
}
